#include<iostream>
#include<fstream>
#include<vector>
using namespace std;

bool IsMine(const int& r, const int& c, vector<vector<char> >& mf){
    if(r<0||c<0) return false;
    return mf[r][c]=='*';
}
int main(){
    int T;
    ifstream fin("in.txt");
    ofstream fout("out.txt");

    fin>>T;

    for(int tt=0;tt<T;tt++){
        int R,C,M;
        fin>>R>>C>>M;
        vector<vector<char> > mf(R,vector<char>(C,'.'));
        mf[R-1][C-1]='c';

        int curr=0;
        int curc=0;
        while(M>0){
            if(R-curr>C-curc){
                for(int i=curc;i<C&&M>0;i++,M--)
                    mf[curr][i]='*';
                curr++;
            }
            else{
                for(int i=curr;i<R&&M>0;i++,M--)
                    mf[i][curc]='*';
                curc++;
            }
        }
        fout<<"Case #"<<tt+1<<": "<<endl;
        if(M!=R*C-1&&(IsMine(R-1,C-2,mf)||IsMine(R-2,C-1,mf)||IsMine(R-2,C-2,mf)))
            fout<<"Impossible"<<endl;
        else {
            for(int i=0;i<mf.size();i++){
                fout<<mf[i][0];
                for(int j=1;j<mf[0].size();j++){
                    fout<<' '<<mf[i][j];
                }
                fout<<endl;
            }
        }
    }

return 0;}
